         * 
         * - Infinite Corridor -
         * The device that traces the year on the 2nd floor of the infinite corridor monitors the flag with a field event. 
         * As such, switching rooms will not reset the device. Therefore, an event that turns off all era flags is placed at the entrance of the 2nd floor.
         * 
         * On the 3rd floor there is a timed event to get the twin statues. Once the timer event with a time limit starts working, it will not disappear unless there is a timer delete event. 
         * Therefore, there is a problem if you go outside the infinite corridor during the time limit. Therefore, during the time limit, the Holy Grail sealing event is activated.
         * 
         * For the same reason, the Holy Grail is sealed when Backbeard appears.
         * 
         * - Spike ceiling -
         * A Holy Grail sealing event is set up so that you cannot teleport with the Holy Grail while a spike ceiling is active.*/


